    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            con=Connect.ConnectDB();
            String name,salary,contact,id,nic,address,date,email;
            
            name=txtName.getText();
            contact=txtContactNo.getText();
            email=txtEmail.getText();
            id=txtID.getText();
            nic=txtNIC.getText();
            address=txtAddress.getText();
            date=txtDate.getText();
            salary=txtSalary.getText();
            
            // Checking whether the necessary fields are empty before inserting data
            if (name.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Nurse Name","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (salary.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Nurse Salary","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (contact.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Contact No.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbGen.getSelectedItem().toString().equals("")){
                JOptionPane.showMessageDialog( this, "Please select Gender.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbGrade.getSelectedItem().toString().equals("")){
                JOptionPane.showMessageDialog( this, "Please select Grade.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (id.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Employee ID","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (nic.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter NIC No.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (address.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Address","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (date.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Date Joined","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

             // Checking whether the entered emp_id is already in the table
            String query1="SELECT emp_id FROM user WHERE emp_id='"+id+"';";
            pst=con.prepareStatement(query1);
            rs=pst.executeQuery(query1);

            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Staff ID already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtID.setText("");
                txtID.requestDefaultFocus();
                return;
            }

            // Query to insert data into employee table
            String query2="INSERT INTO employee(emp_id,emp_name,emp_nic,emp_tel,emp_address,emp_email,emp_gender,date_joined,emp_type,emp_salary)"
            + "VALUES('"+ id + "','"+ name + "','"+ nic + "','" + contact + "','"+ address+ "','"
            + email + "','"+ cmbGen.getSelectedItem().toString()+ "','" + date + "','Maintenance Staff','"+salary+"')";

            // Query to insert data into maintenance_staff table
            String query3="INSERT INTO maintenance_staff VALUES('"+id+"','"
                    +txtQualifications.getText()+"','"+cmbGrade.getSelectedItem().toString()+"')";
            
            pst=con.prepareStatement(query2);
            pst.execute();
            
            pst=con.prepareStatement(query3);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Successfully Registered","Success",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);

        } catch (HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    // To delete a particular record regarding a doctor
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        try {
            int x = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirm",JOptionPane.YES_NO_OPTION);
            if (x==0)
            {
                con=Connect.ConnectDB();

                // Query to delete details from employee table
                String query1= "DELETE FROM employee WHERE emp_id = '" + txtID.getText() + "'";
                
                // Query to delete details from maintenance_staff table
                String query2= "DELETE FROM maintenance_staff WHERE emp_id = '" + txtID.getText() + "'";
                
                pst=con.prepareStatement(query1);
                pst.execute();

                pst=con.prepareStatement(query2);
                pst.execute();

                JOptionPane.showMessageDialog(this,"Successfully Deleted","Success",JOptionPane.INFORMATION_MESSAGE);
                this.clear();
            }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

