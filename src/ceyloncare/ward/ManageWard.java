    // Generates new ward_no for each new ward record
    private void generateID() {
        try{
            con = Connect.ConnectDB();
            String query = "SELECT ward_no FROM ward ORDER BY ward_no";
            pst=con.prepareStatement(query);
            rs=pst.executeQuery();
            rs.last();
            countWard = Integer.parseInt(rs.getString(1))+1;
            txtWardNo.setText(Integer.toString(countWard));
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }
