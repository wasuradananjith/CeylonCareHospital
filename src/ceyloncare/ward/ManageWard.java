    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            con=Connect.ConnectDB();
            String wardNo,wardType,beds,charge;
            wardNo=txtWardNo.getText();
            wardType=lbWardType.getText();
            beds=txtBeds.getText();
            charge=txtCharge.getText();

            // Checking whether the necessary fields are empty before inserting data
            if (wardNo.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Ward Number","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (wardType.equals("")) {
                JOptionPane.showMessageDialog( this, "Please Select Ward Type","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (beds.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Number of Beds","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
               
            if (charge.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Charge per Bed","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Query to insert data into ward table
            String query1= "INSERT INTO ward(ward_no,no_of_beds,spec_name,charge_per_bed)"
            + "VALUES('"+ wardNo + "','" + beds + "','" + wardType + "','" + charge + "')";

            pst=con.prepareStatement(query1);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Successfully Inserted","Success",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);

        } catch (HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    // To delete a particular record regarding a ward
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        try {
            int x = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirm",JOptionPane.YES_NO_OPTION);
            if (x==0)
            {
                con=Connect.ConnectDB();
                String query1= "DELETE FROM ward WHERE ward_no = '" + txtWardNo.getText() + "'";
                pst=con.prepareStatement(query1);
                pst.execute();
                JOptionPane.showMessageDialog(this,"Successfully Deleted","Success",JOptionPane.INFORMATION_MESSAGE);
                this.clear();
            }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

