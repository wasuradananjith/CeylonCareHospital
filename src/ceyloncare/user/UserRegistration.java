    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            con=Connect.ConnectDB();
            String name,username,password,email,contact,id,nic,address,date;
            
            name=txtName.getText();
            username=txtUsername.getText();
            password=String.valueOf(txtPassword.getPassword());
            email=txtEmail.getText();
            contact=txtContactNo.getText();
            id=txtID.getText();
            nic=txtNIC.getText();
            address=txtAddress.getText();
            date=txtDate.getText();
           
            // Checking whether the necessary fields are empty before inserting data
            if (name.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Name","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (username.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Username","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (password.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Password","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (contact.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Contact No.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbGen.getSelectedItem().toString().equals("")){
                JOptionPane.showMessageDialog( this, "Please select Gender.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (id.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Employee ID","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (nic.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter NIC No.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (address.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Address","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (date.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Date Joined","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (email.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Email Address","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Checking whether the entered username is already in the table
            String query1="SELECT username FROM user WHERE username='"+username+"';";
            pst=con.prepareStatement(query1);
            rs=pst.executeQuery(query1);
            
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Username already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtUsername.setText("");
                txtUsername.requestDefaultFocus();
            return;
            }
            
            // Checking whether the entered emp_id is already in the table
            String query4 = "SELECT emp_id FROM employee WHERE emp_id='"+id+"';";
            pst=con.prepareStatement(query1);
            rs=pst.executeQuery(query4);
            
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Employee ID already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtID.setText("");
                txtID.requestDefaultFocus();
            return;
            }
            
            // Query to insert data into user table
            String query2= "INSERT INTO user(username,password,name_user,type,emp_id)"
                    + "VALUES('"+ username + "','" 
                    + password + "','" + name + "','"+ choice+ "','" + id + "')";
            
            // Query to insert data into employee table
            String query3="INSERT INTO employee(emp_id,emp_name,emp_nic,emp_tel,emp_address,emp_email,emp_gender,date_joined,emp_type)"
                    + "VALUES('"+ id + "','"+ name + "','"+ nic + "','" + contact + "','"+ address+ "','" 
                    + email + "','"+ cmbGen.getSelectedItem().toString()+ "','" + date + "','" + choice +"')";
            
            pst=con.prepareStatement(query2);
            pst.execute();
            
            pst=con.prepareStatement(query3);
            pst.execute();
            
            JOptionPane.showMessageDialog(this,"Successfully Registered","Success",JOptionPane.INFORMATION_MESSAGE); 
            btnSave.setEnabled(false);
            
        } catch (HeadlessException | SQLException ex){
           JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

