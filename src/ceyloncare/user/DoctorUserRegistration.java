    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            con=Connect.ConnectDB();
            String name,username,password,id;
            username=txtUsername.getText();
            password=String.valueOf(txtPassword.getPassword());
            name=lbName.getText();
            id=lbID.getText();
            
            // Checking whether the necessary fields are empty before inserting data
            if (username.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Username","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (password.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Password","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (id.equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Employee ID","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Checking whether the entered username is already in the table
            String query1="SELECT username FROM user WHERE username= '" + username + "'";
            pst=con.prepareStatement(query1);
            rs=pst.executeQuery(query1);

            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Username already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtUsername.setText("");
                txtUsername.requestDefaultFocus();
                return;
            }
  
            // Query to insert data into user table
            String query2= "INSERT INTO user(username,password,name_user,type,emp_id)"
            + "VALUES('"+ username + "','"
            + password + "','" + name + "','"+ choice+ "','" + id + "')";

            pst=con.prepareStatement(query2);
            pst.execute();

            JOptionPane.showMessageDialog(this,"Successfully Registered","Success",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);

        } catch (HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    // To delete a particular record regarding a doctor-user
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        try {
            int x = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirm",JOptionPane.YES_NO_OPTION);
            if (x==0)
            {
                con=Connect.ConnectDB();

                // Query to delete details from user table
                String query1= "DELETE FROM user WHERE username = '" + txtUsername.getText() + "'";
                pst=con.prepareStatement(query1);
                pst.execute();

                JOptionPane.showMessageDialog(this,"Successfully Deleted","Success",JOptionPane.INFORMATION_MESSAGE);
                this.clear();
            }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

