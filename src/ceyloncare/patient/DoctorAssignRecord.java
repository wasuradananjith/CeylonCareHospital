    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            con=Connect.ConnectDB();
            
            // Select the particular row
            int row= jTable1.getSelectedRow();
            String table_click= jTable1.getModel().getValueAt(row, 0).toString();
            
            // Selecting data for a particular emp_id
            String query= "SELECT a.admit_id,n.doc_id,e.emp_name,p.patient_id,p.patient_name "
                    + "FROM patient p,admission a,patient_doc_assign n,employee e "
                    + "WHERE "
                    + "n.doc_id=e.emp_id AND a.admit_id=n.admit_id "
                    + "AND a.patient_id=p.patient_id AND a.admit_id='"+table_click+"'";
            pst=con.prepareStatement(query);
            rs=  pst.executeQuery();
            
             // Add data to the required fields in the created Form Object
            if(rs.next()){
                    this.hide();
                    // Create a new object from DoctorAssign Form Class
                    DoctorAssign frm = new DoctorAssign(null,null,null,null,2);
                    frm.setVisible(true);

                    String addAdmitID=rs.getString("admit_id");
                    frm.txtAdmitNo.setText(addAdmitID);

                    String addPatientID=rs.getString("patient_id");
                    frm.lbPatientID.setText(addPatientID);

                    String addPatientName=rs.getString("patient_name");
                    frm.lbPatientName.setText(addPatientName);

                    String addDocID=rs.getString("doc_id");
                    frm.txtDocID.setText(addDocID);
                    
                    String addDocName=rs.getString("emp_name");
                    frm.lbDocName.setText(addDocName);

                    frm.btnSave.setEnabled(false);
                    frm.btnDelete.setEnabled(true);
                
            }

        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(this,"No row selected!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

