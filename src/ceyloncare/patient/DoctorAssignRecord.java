    // Search patient, admission, patient_doc_assign and employee tables
    private void search() {
        try{
            con=Connect.ConnectDB();
            String query="SELECT a.admit_id,n.doc_id,e.emp_name,p.patient_id,p.patient_name "
                    + "FROM patient p,admission a,patient_doc_assign n,employee e "
                    + "WHERE (p.patient_id LIKE '%"+txtSearch.getText()+"%' OR p.patient_name LIKE '%"
                    +txtSearch.getText()+"%' OR e.emp_name LIKE '%"+txtSearch.getText()+"%' OR "
                    + "a.admit_id LIKE '%"+txtSearch.getText()+"%' OR "
                    +"n.doc_id LIKE '%"+ txtSearch.getText()+"%') AND "
                    + "n.doc_id=e.emp_id AND a.admit_id=n.admit_id AND a.patient_id=p.patient_id";
                    
            pst=con.prepareStatement(query);
            rs=pst.executeQuery();
            DefaultTableModel registrationModel = (DefaultTableModel)jTable1.getModel();
            registrationModel.setRowCount(0);
            while(rs.next())
            {
                registrationModel.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),
                rs.getString(4),rs.getString(5)});
            }
        } catch (SQLException e) {
           JOptionPane.showMessageDialog(null, e);
        }
    }

