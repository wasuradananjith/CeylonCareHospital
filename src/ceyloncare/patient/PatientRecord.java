    // Add data to the Patient/PatientAdmit Form (when btnAdd button is clicked) based on the passed flag value
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            con=Connect.ConnectDB();
            
            // Select the particular row
            int row= jTable1.getSelectedRow();
            String table_click= jTable1.getModel().getValueAt(row, 0).toString();
            
            // Selecting data for a particular patient_id
            String query= "SELECT * FROM patient WHERE patient_id= '" + table_click + "'";
            pst=con.prepareStatement(query);
            rs=  pst.executeQuery();
            
            // Add data to the required fields in the created Form Object
            if(rs.next()){
                // flag=2 means the previous control flow was passed from the Patient Form
                if(flag==2){
                    this.hide();
                    // Create a new object from Patient Form Class
                    Patient frm = new Patient(choice);
                    frm.setVisible(true);
                    
                    String addID=rs.getString("patient_id");
                    frm.txtID.setText(addID);

                    String addName=rs.getString("patient_name");
                    frm.txtName.setText(addName);

                    String addNIC=rs.getString("patient_nic");
                    frm.txtNIC.setText(addNIC);

                    String addGender=rs.getString("patient_gender");
                    frm.cmbGender.setSelectedItem(addGender);
                    
                    String addBG=rs.getString("patient_bg");
                    frm.cmbBloodGroup.setSelectedItem(addBG);
                    
                    String addAge=rs.getString("patient_age");
                    frm.txtAge.setText(addAge);
                    
                    String addGuardian=rs.getString("patient_guardian");
                    frm.txtGuardian.setText(addGuardian);
                    
                    String addPhone=rs.getString("patient_phone");
                    frm.txtContactNo.setText(addPhone);
                    
                    String addAddress=rs.getString("patient_address");
                    frm.txtAddress.setText(addAddress);

                    String addEmail=rs.getString("patient_email");
                    frm.txtEmail.setText(addEmail);

                    frm.btnSave.setEnabled(false);
                    frm.btnDelete.setEnabled(true);
                    frm.btnUpdate.setEnabled(true);
                }
                // flag=1 means the previous control flow was passed from the PatientAdmit Form
                else if (flag==1){
                    this.hide();
                    // Create a new object from PatientAdmit Form Class
                    PatientAdmit frm = new PatientAdmit(choice,null,null,1);
                    frm.setVisible(true);
                    
                    String addID=rs.getString("patient_id");
                    frm.txtPatientID.setText(addID);
                    
                    String addName=rs.getString("patient_name");
                    frm.txtName.setText(addName);
                    
                }
            }

        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(this,"No row selected!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

