    // Search patient table
    private void search() {
        try{
            con=Connect.ConnectDB();
            String query="SELECT * FROM patient "
                    + "WHERE patient_id LIKE '%"+txtSearch.getText()+"%' OR patient_name LIKE '%"
                    +txtSearch.getText()+"%' OR patient_nic LIKE '%"+txtSearch.getText()+"%' OR "
                    + "patient_gender LIKE '%"+txtSearch.getText()+"%' OR "
                    +"patient_bg LIKE '%"+ txtSearch.getText()+"%' OR "
                    +"patient_age LIKE '%"+txtSearch.getText()+"%' OR "
                    +"patient_guardian LIKE '%"+txtSearch.getText()+"%' OR "
                    +"patient_phone LIKE '%"+txtSearch.getText()+"%' OR "
                    +"patient_address LIKE '%"+txtSearch.getText()+"%' OR "
                    +"patient_email LIKE '%"+txtSearch.getText()+"%'";
                    
            pst=con.prepareStatement(query);
            rs=pst.executeQuery();
            DefaultTableModel registrationModel = (DefaultTableModel)jTable1.getModel();
            registrationModel.setRowCount(0);
            while(rs.next())
            {
                registrationModel.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),
                rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),
                rs.getString(8),rs.getString(9),rs.getString(10)});
            }
        } catch (SQLException e) {
           JOptionPane.showMessageDialog(null, e);
        }
    }

